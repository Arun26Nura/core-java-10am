package com.besant.core.operators;

public class UnaryOperators {

	public static void main(String[] args) {
		int a=5;
		//a= a+1; // a+=1;
		//a++;
		// Post operators // Pre operators.
		// Pre incrementor
		++a;	//6	a=1+a;
		// post incrementor
		a++; //7  a=a+1;

		// pre decrementor
		
		--a;  //a=-1+a;
		// post decrement.
		a--; //a=a-1;
	}

}
