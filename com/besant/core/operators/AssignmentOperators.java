package com.besant.core.operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a;
		int b=25;
		
		a= 10;
		a+=b;// a=+b;
		
		
		System.out.println(a);//35
		a-=5;// a=+b;
		System.out.println(a); //30
		a*=2;// a=+b;
		System.out.println(a);
		a/=2;// a=+b;
		System.out.println(a);
		a%=7;// a=+b;
		System.out.println(a);
		
		a*=b;
		

	}

}
