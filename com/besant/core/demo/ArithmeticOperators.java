package com.besant.core.demo;

public class ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic  +,-,*,/,%
        int a=10;
        short b=3;

        int sumVal=a+b; // 13
        System.out.println(sumVal);
        int subVal=a-b; // 7
        System.out.println(subVal);
        int mulVal=a*b; // 30
        System.out.println(mulVal);
        int divVal=a/b; // 3
        System.out.println(divVal);
        double divVal2=10d/b; // 3.33
        System.out.println(divVal2);
        int modulo=a%b; // 1
        System.out.println(modulo);


    }
}
